var map = [], states = [], mapSize = input.length;

function readInput() {
    for (var y = 0; y < input.length; y++) {
        var line = input[y];
        map[y] = [];
        for (var x = 0; x < line.length; x++) {
            map[y][x] = line[x];
        }
    }
}

function getAdjacent(map, xx, yy) {
    var adj = [];
    for (var i = -1; i <= 1; i++) {
        var y = yy+i;
        if (y < 0 || y >= mapSize) continue;
        for (var j = -1; j <= 1; j++) {
            var x = xx+j;
            if (x < 0 || x >= mapSize) continue;
            if ((i == 0) && (j == 0)) continue;
            adj.push(map[y][x]);
        }
    }
    return adj;
}

function cmpStates(s1,s2) {
    var res = true;
    for (var y = 0; y < mapSize; y++) {
        for (var x = 0; x < mapSize; x++) {
            if (s1[y][x] != s2[y][x]) {
                res = false;
                break;
            }
        }
    }
    return res;
}

function nextState(lastState) {
    var newState = $.extend(true, [], lastState);

    for (var y = 0; y < mapSize; y++) {
        for (var x = 0; x < mapSize; x++) {
            var adj = getAdjacent(lastState, x, y);
            var trees = 0, lumbs = 0, alen = adj.length;
            for (var i = 0; i < alen; i++) {
                if (adj[i] == '|') trees++;
                if (adj[i] == '#') lumbs++;
            }

            // rule 1: . ->  | if 3 or more adjanced are trees
            if (lastState[y][x] == '.') {
                if (trees >= 3) newState[y][x] = '|';
            } else 
            // rule 2: | => #
            if (lastState[y][x] == '|') {
                if (lumbs >= 3) newState[y][x] = '#';
            } else 
            // rule 3: # -> # or .
            if (lastState[y][x] == '#') {
                if ((lumbs >= 1) && (trees >= 1)) newState[y][x] = '#'; else newState[y][x] = '.';
            }

        }
    }

    for (var i = 0; i < states.length;i++) {
        if (cmpStates(states[i], newState)) {
            console.log('mame duplicitu!!', i, states.length, states[i], newState);
            console.log('resource value', getCount(newState, '|')*getCount(newState, '#'));
        }
    }

    states.push(newState);

    return newState;
};


function getCount(map, what) {
    var count = 0;
    for (var y = 0; y < mapSize; y++) {
        for (var x = 0; x < mapSize; x++) {
            if (map[y][x] == what) count++;
        }
    }

    return count;
};

