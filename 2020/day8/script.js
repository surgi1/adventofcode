function run(dat) {
    var ptr = 0;
    var acc = 0;

    function tick() {
        if (!dat[ptr]) {
            return 'EOF';
        }
        if (dat[ptr].executed) {
            return 'INF';
        }

        dat[ptr].executed = true;
        
        switch (dat[ptr].i) {
            case 'nop': 
                ptr++;
                break;
            case 'acc':
                acc = acc+dat[ptr].v;
                ptr++;
                break;
            case 'jmp':
                ptr = ptr+dat[ptr].v;
                break;
        }
        return true;
    }

    var cnt = true;
    while (cnt) {
        var tickCode = tick();
        if (tickCode !== true) cnt = false;
    }
    //console.log(tickCode, 'acc value', acc, 'ptr value', ptr, dat);

    return {code: tickCode, acc: acc, ptr: ptr};
}


